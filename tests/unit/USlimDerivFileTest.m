    "cadaoutput1 = ADiGator_sub(cadainput1);"; ...
    "cada1d1 = zeros(2,1);"; ...
    "for cadaforcount1 = 1:2"; ...
    "  cada1d1(cadaforcount1) = cadaoutput1.dx(cadaforcount1);"; ...
    "end"; ...
    "y.dx = cada1d1;"; ...
